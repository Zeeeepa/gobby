"""Database migrations for local storage.

This module handles schema migrations for the Gobby database.

For new databases (version == 0):
    BASELINE_SCHEMA is applied, jumping directly to BASELINE_VERSION (171).

For existing databases at v171+:
    Any migrations in MIGRATIONS (v172+) are applied incrementally.

To add a new migration:
    1. Add it to the MIGRATIONS list below with version = 172, 173, etc.
    2. Use SQL strings for schema changes, callables for data migrations.
    3. Also add the migration to BASELINE_SCHEMA for future fresh installs.
"""

import logging
from collections.abc import Callable
from pathlib import Path

from gobby.storage.database import LocalDatabase

logger = logging.getLogger(__name__)


class MigrationUnsupportedError(Exception):
    """Raised when database version is too old to migrate."""

    pass


# Migration can be SQL string or a callable that takes LocalDatabase
MigrationAction = str | Callable[[LocalDatabase], None]

# Baseline version - the schema state that is applied for new databases directly.
# Must be bumped when BASELINE_SCHEMA is updated with columns from new migrations,
# so that fresh databases don't re-run migrations already baked into the baseline.
BASELINE_VERSION = 180

# Minimum migration version - databases older than this cannot be upgraded
# because legacy migrations (pre-v171) have been removed.
_MIN_MIGRATION_VERSION = 171

# Baseline schema - flattened at v171, includes all migrations through v171
# This is applied for new databases directly
# Generated by: sqlite3 ~/.gobby/gobby-hub.db .schema
BASELINE_SCHEMA = (Path(__file__).parent / "baseline_schema.sql").read_text()


def _setup_code_symbols_fts(db: LocalDatabase) -> None:
    """Create FTS5 triggers and populate from existing data.

    The FTS5 virtual table itself is created in BASELINE_SCHEMA (or the
    migration SQL), but triggers contain semicolons inside BEGIN...END
    which break the naive ';'-split parser. So we use executescript() here.
    """
    conn = db.connection
    conn.executescript("""
        CREATE VIRTUAL TABLE IF NOT EXISTS code_symbols_fts USING fts5(
            name, qualified_name, signature, docstring,
            content='code_symbols', content_rowid='rowid'
        );

        CREATE TRIGGER IF NOT EXISTS code_symbols_ai AFTER INSERT ON code_symbols BEGIN
            INSERT INTO code_symbols_fts(rowid, name, qualified_name, signature, docstring)
            VALUES (new.rowid, new.name, new.qualified_name, new.signature, new.docstring);
        END;

        CREATE TRIGGER IF NOT EXISTS code_symbols_ad AFTER DELETE ON code_symbols BEGIN
            INSERT INTO code_symbols_fts(code_symbols_fts, rowid, name, qualified_name, signature, docstring)
            VALUES ('delete', old.rowid, old.name, old.qualified_name, old.signature, old.docstring);
        END;

        CREATE TRIGGER IF NOT EXISTS code_symbols_au AFTER UPDATE ON code_symbols BEGIN
            INSERT INTO code_symbols_fts(code_symbols_fts, rowid, name, qualified_name, signature, docstring)
            VALUES ('delete', old.rowid, old.name, old.qualified_name, old.signature, old.docstring);
            INSERT INTO code_symbols_fts(rowid, name, qualified_name, signature, docstring)
            VALUES (new.rowid, new.name, new.qualified_name, new.signature, new.docstring);
        END;

        INSERT OR IGNORE INTO code_symbols_fts(rowid, name, qualified_name, signature, docstring)
        SELECT rowid, name, qualified_name, signature, docstring FROM code_symbols;
    """)


def _setup_code_content_fts(db: LocalDatabase) -> None:
    """Create FTS5 virtual table and triggers for code content chunks.

    Follows the same pattern as _setup_code_symbols_fts.
    """
    conn = db.connection
    conn.executescript("""
        CREATE VIRTUAL TABLE IF NOT EXISTS code_content_fts USING fts5(
            content, file_path, language,
            content='code_content_chunks', content_rowid='rowid'
        );

        CREATE TRIGGER IF NOT EXISTS code_content_ai AFTER INSERT ON code_content_chunks BEGIN
            INSERT INTO code_content_fts(rowid, content, file_path, language)
            VALUES (new.rowid, new.content, new.file_path, new.language);
        END;

        CREATE TRIGGER IF NOT EXISTS code_content_ad AFTER DELETE ON code_content_chunks BEGIN
            INSERT INTO code_content_fts(code_content_fts, rowid, content, file_path, language)
            VALUES ('delete', old.rowid, old.content, old.file_path, old.language);
        END;

        CREATE TRIGGER IF NOT EXISTS code_content_au AFTER UPDATE ON code_content_chunks BEGIN
            INSERT INTO code_content_fts(code_content_fts, rowid, content, file_path, language)
            VALUES ('delete', old.rowid, old.content, old.file_path, old.language);
            INSERT INTO code_content_fts(rowid, content, file_path, language)
            VALUES (new.rowid, new.content, new.file_path, new.language);
        END;

        INSERT OR IGNORE INTO code_content_fts(rowid, content, file_path, language)
        SELECT rowid, content, file_path, language FROM code_content_chunks;
    """)


def _drop_summary_column(db: LocalDatabase) -> None:
    """Drop summary column from code_symbols and rebuild FTS without it."""
    conn = db.connection
    # Drop triggers and FTS first — triggers reference summary, blocking DROP COLUMN
    conn.executescript("""
        DROP TRIGGER IF EXISTS code_symbols_ai;
        DROP TRIGGER IF EXISTS code_symbols_ad;
        DROP TRIGGER IF EXISTS code_symbols_au;
        DROP TABLE IF EXISTS code_symbols_fts;
    """)
    # Now safe to drop the column
    conn.execute("ALTER TABLE code_symbols DROP COLUMN summary")
    # Rebuild FTS and triggers without summary
    _setup_code_symbols_fts(db)


